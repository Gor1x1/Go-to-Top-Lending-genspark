/**
 * Idempotent seed for the Phase 4 chrome + subpage CMS blocks.
 *
 * Uses `INSERT OR IGNORE` so:
 *   - first call on a fresh DB inserts every row,
 *   - subsequent calls become no-ops for blocks the admin already edited
 *     (the UNIQUE constraint on `block_key` rejects the re-insert).
 *
 * Returns the counts so the caller can surface progress feedback.
 */
export async function seedProBlocks(
  db: D1Database
): Promise<{ total: number; inserted: number; error?: string }> {
  for (const b of PRO_BLOCKS) {
    if (b.textsRu.length !== b.textsAm.length) {
      return {
        total: PRO_BLOCKS.length,
        inserted: 0,
        error: `Block ${b.key}: texts_ru/texts_am length mismatch (${b.textsRu.length} vs ${b.textsAm.length})`
      }
    }
  }
  let inserted = 0
  for (const b of PRO_BLOCKS) {
    const res = await db.prepare(
      'INSERT OR IGNORE INTO site_blocks (block_key, page, block_type, title_ru, title_am, texts_ru, texts_am, images, buttons, custom_css, custom_html, is_visible, sort_order, social_links, text_styles) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)'
    ).bind(
      b.key, b.page, 'subpage', b.titleRu, b.titleAm,
      JSON.stringify(b.textsRu), JSON.stringify(b.textsAm),
      '[]', '[]', '', '{}', 1, b.sort, '[]', '[]'
    ).run()
    const changes = res?.meta && (res.meta as any).changes
    if (typeof changes === 'number' && changes > 0) inserted++
  }
  return { total: PRO_BLOCKS.length, inserted }
}
